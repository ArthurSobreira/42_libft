int	ft_isalpha(int value)
{
	if ((value >= 'A') && (value <= 'Z'))
	{
		return (1);
	}
	else if ((value >= 'a') && (value <= 'z'))
	{
		return (1);
	}
	else
	{
		return (0);
	}
}
